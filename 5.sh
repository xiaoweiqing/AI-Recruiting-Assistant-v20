#!/bin/bash
# ==============================================================================
#      AI 招聘助理 - 终极启动器 (v8.0 - 环境变量修复版)
# ==============================================================================
#
# 特性:
# - [终极修复] 通过设置 HF_HOME 环境变量，明确告知虚拟环境内的程序
#   去哪里寻找全局模型缓存，彻底解决路径/权限冲突导致的加载死锁问题。
# - [标准实践] 保持程序依赖在虚拟环境内隔离，模型在全局共享的最佳实践。
#
# ==============================================================================

# --- 1. 用户配置区 ---
PYTHON_SCRIPT_NAME="16.py" # 依然使用我们最稳定的 v5.0 同步脚本
# 【【【 关键配置 】】】
# 设置 Hugging Face 的全局缓存根目录
export HF_HOME="$HOME/.cache/huggingface"

# --- 2. 自动切换到脚本所在目录 ---
cd "$(dirname "$0")" || exit

# --- 脚本主逻辑 ---
clear
echo "========================================================"
echo "    AI 招聘助理 - 终极启动器 (v8.0 - 环境变量修复版)"
echo "========================================================"
echo ""

# --- 步骤 1: 准备 Python 虚拟环境 & 依赖 ---
echo "[1/2] 正在准备 Python 虚拟环境并安装依赖..."
if [ ! -d "venv" ]; then
    echo ">> 虚拟环境不存在，正在自动创建..."
    python3 -m venv venv
fi
source "venv/bin/activate"

if [ ! -f "requirements.txt" ]; then
    echo "❌ 严重错误: 未找到 'requirements.txt' 文件！"
    read -p "按任意键退出..."
    exit 1
fi
pip install -i "https://pypi.tuna.tsinghua.edu.cn/simple" -r requirements.txt > /dev/null
echo "✅ Python 环境已在虚拟环境中就绪。"
echo ""

# --- 步骤 2: 启动 Python 主程序 ---
echo "[2/2] 正在启动 Python 主程序..."
echo "✅ 已设置全局模型缓存路径: ${HF_HOME}"
echo "-------------------------- [ 程序日志开始 ] --------------------------"
echo ""

# 使用 venv 环境中的 python3 来运行脚本
python3 "${PYTHON_SCRIPT_NAME}"

# --- 脚本结束后的提示 ---
echo ""
echo "-------------------------- [ 程序日志结束 ] --------------------------"
deactivate
echo ""
echo "程序已结束。"
read -p "按任意键关闭此终端..."