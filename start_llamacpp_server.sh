#!/bin/bash
# ==============================================================================
#      Llama.cpp 服务器 - 启动器 v2.0
# ==============================================================================
#
#  这个脚本只负责一件事：启动 Llama.cpp 的后台服务器。
#  请保持这个终端窗口的运行状态，不要关闭它。
#
# ==============================================================================

# --- 用户配置区 ---
# 【请确认这两个路径是正确的】
LLAMACPP_SERVER_EXECUTABLE="/home/weiyubin/llama.cpp/build/bin/llama-server"
MODEL_PATH="/home/weiyubin/models/gpt-oss-20b-Q4_K_M.gguf"

# --- 脚本主逻辑 ---
clear
echo "========================================================"
echo "         Llama.cpp 服务器启动器 v2.0"
echo "========================================================"
echo ""

# 步骤 1: 检查服务器程序是否存在
echo "[1/2] 正在检查 Llama.cpp 服务器程序..."
if [ ! -f "$LLAMACPP_SERVER_EXECUTABLE" ]; then
    echo "❌ 错误: 未在以下路径找到 Llama.cpp 服务器程序: "
    echo "   -> ${LLAMACPP_SERVER_EXECUTABLE}"
    echo "   -> 请确认 Llama.cpp 已被正确编译，并且路径配置正确。"
    read -p "按任意键退出..."
    exit 1
fi
echo "✅ 服务器程序已找到。"
echo ""

# 步骤 2: 启动服务器
echo "[2/2] 正在启动 Llama.cpp 服务器..."
echo "模型路径: ${MODEL_PATH}"
echo "--------------------------------------------------------"
echo "服务器启动后，请将此终端保持运行状态。"
echo "然后打开一个新的终端来运行您的 Python 脚本。"
echo "--------------------------------------------------------"
echo ""

# 执行启动命令
"$LLAMACPP_SERVER_EXECUTABLE" -m "$MODEL_PATH" -c 4096 -t 8 --host 0.0.0.0 --port 8080

echo "服务器已停止。"